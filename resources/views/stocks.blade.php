    function yazdir(){
        $.ajax({
            url: '{{route('stockvalues')}}',
            dataType: "json",
            success: function (response) {
                document.getElementById("stocksvalue").innerHTML=""
                for(obj in response){
                    let satir = $("<tr>")
                    let hucre = $("<td>").text(obj)
                    satir.append(hucre)
                    for(deger in response[obj]){
